export function mapLocalIntent(localIntent){
    
    const keys = Object.keys(localIntents);
    for (var i=0;i<keys.length;i++){
        //If we need to add the expressions
        if(keys[i].match(/{expression}/)){
            for(var k=0;k++;k<expressions.length){
                const newKey = keys[i]
                .replace('[`','.')
                .replace('`]','')
                .replace('${expression}',expressions[k]);
                const newVal = localIntents[keys[i]].replace('[`','.')
                .replace('`]','')
                .replace('${expression}',expressions[k]);
                localIntents[newKey] = newVal;
            }
        }
    }

    if(typeof(localIntents[localIntent])!=="undefined"){
        return localIntents[localIntent];
    } else {
        return null; 
    }
}